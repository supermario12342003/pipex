#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		lens1;
	int		i;

	lens1 = ft_strlen(s1);
	s2 = malloc((lens1 + 1) * sizeof (char));
	if (!s2)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
